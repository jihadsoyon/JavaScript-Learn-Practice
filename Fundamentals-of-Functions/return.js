function tenTimes(number){
  const result = number * 10;
  return result;
}

function cuthalf(number){
  const half =  number / 2;
  return half;
}

function bisMillah(number){
  const utor = number + 70;
  return utor;
}

function allhuAkbar(number){
  const niyom = number * 100;
  return niyom;
}

const output = tenTimes(5);
console.log('output', output)

